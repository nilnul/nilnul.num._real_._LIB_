namespace nilnul.num.real.of_.unary_
{
	public class _NeutralizeX
	{
		/// <summary>
		/// the result is called drift
		/// </summary>
		/// <param name="anchor">origin/anchor
		///		anchor which is correspondent with drift, <see cref="_Drift_0anchor(double, double)"/>
		/// </param>
		/// <param name="x"></param>
		/// <returns></returns>
		static public double _Drift_0anchor(double anchor, double x) {
			return x - anchor;
		}
	}
}
